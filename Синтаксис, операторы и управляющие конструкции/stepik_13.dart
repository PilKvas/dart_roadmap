// Используя оператора switch-case выведите в терминал уведомление том, какое значение из диапазона от 0 до 5 подается на его вход.

void main(){
  int num = 5;
  switch(num){
    case 5:
      print('5');
      break;
    case 4:
      print('4');
      break;
    case 3:
      print('3');
      break;
    case 2:
      print('2');
      break;
    case 1:
      print('1');
      break;
    case 0:
      print('0');
      break;
  }
}